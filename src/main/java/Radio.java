public class Radio {
    private int radio;
    private int volume;

    public Radio(int radio, int volume) {
        this.radio = radio;
        this.volume = volume;
    }

    public int getRadio() {
        return radio;
    }

    public void setRadio(int radio) {
        this.radio = radio;
    }

    public int getVolume() {
        return volume;
    }

    public void setVolume(int volume) {
        this.volume = volume;
    }
}
